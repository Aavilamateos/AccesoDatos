package ceufct.exception;

public class FechasServices extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -9203305182014913995L;

	public FechasServices() {
	}

	public FechasServices(String message) {
		super(message);
	}

	public FechasServices(Throwable cause) {
		super(cause);
	}

	public FechasServices(String message, Throwable cause) {
		super(message, cause);
	}

	public FechasServices(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
